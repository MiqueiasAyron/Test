#Aula006-Tipos Primitivos
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
s = n1 + n2
print('A soma vale', s)
# O tipos primitivos básicos são int, float, bool, str
# int: variáveis númericas de valor inteiro
# float: variáveis numéricas de valor real
# bool: variáveis booleanas (True, False) 
# str: strings( variáveis entre ''. palavras )
print('A soma vale{}'.format(s)) #sintaxe
print('A sima entre {} e {} vale {}'.format(n1,n2,s))

